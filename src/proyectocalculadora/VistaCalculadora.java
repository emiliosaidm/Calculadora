    private void buttonEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEqualsActionPerformed
        String[] resp;
        resp = this.calculadora.calcularExpresion(this.expresion.toString());
        if(resp[0].equals("1")){
            this.resultWindow.setText(resp[1]);
            this.deleteExpresion();
            this.expresion.append(resp[1]);
        }else{
            this.resultWindow.setText("Cadena inconsistente");
            this.deleteExpresion();
        }
        
    }//GEN-LAST:event_buttonEqualsActionPerformed

